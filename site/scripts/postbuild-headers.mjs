// Generates dist/_headers after the Astro build.
//
// Netlify's _headers file only supports full-segment placeholders and trailing
// splats, so per-file rules for the raw-markdown twins (/skills/<slug>.md) are
// emitted here as exact paths, derived from the same skills/ directory the
// site is built from.

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const siteRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const skillsDir = path.resolve(siteRoot, '../skills');
const distDir = path.join(siteRoot, 'dist');

const SITE_URL = 'https://hubspot.granot.io';
const STAGES = ['before', 'execute', 'after'];

const slugs = fs
  .readdirSync(skillsDir, { withFileTypes: true })
  .filter((entry) => entry.isDirectory() && fs.existsSync(path.join(skillsDir, entry.name, 'SKILL.md')))
  .map((entry) => entry.name)
  .sort();

const lines = [
  '# Generated by scripts/postbuild-headers.mjs — do not edit dist/_headers directly.',
  '',
  '/_astro/*',
  '  Cache-Control: public, max-age=31536000, immutable',
  '',
  '/*',
  '  Access-Control-Allow-Origin: *',
  '',
  '/llms.txt',
  '  Content-Type: text/markdown; charset=utf-8',
  '',
  '/llms-full.txt',
  '  Content-Type: text/markdown; charset=utf-8',
  '',
];

for (const slug of slugs) {
  lines.push(
    `/skills/${slug}.md`,
    '  Content-Type: text/markdown; charset=utf-8',
    `  Link: <${SITE_URL}/skills/${slug}>; rel="canonical"`,
    ''
  );
  for (const stage of STAGES) {
    if (fs.existsSync(path.join(skillsDir, slug, 'scripts', `${stage}.py`))) {
      lines.push(`/skills/${slug}/scripts/${stage}.py`, '  Content-Type: text/x-python; charset=utf-8', '');
    }
  }
}

fs.writeFileSync(path.join(distDir, '_headers'), lines.join('\n'));
console.log(`postbuild-headers: wrote ${lines.length} lines for ${slugs.length} skills to dist/_headers`);
